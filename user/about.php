             The first blood bank in the United States was established in 1937 by Dr. Bernard Fantus at Cook County Hospital in Chicago.</p>
    <p>A blood bank is a place where blood is collected and stored before it is used for transfusions. Blood banking takes place 
        in the lab. This is to make sure that donated blood and blood products are safe before they are used. Blood banking also
         determines the blood type. The blood is also tested for infectious diseases.</p>

         <h4>Storage and Management
         </h4>
         <p>Routine blood storage is 42 days or 6 weeks for stored packed red blood cells (also called "StRBC" or "pRBC"), by far the most commonly transfused blood product, and involves refrigeration but usually not freezing.
             There has been increasing controversy about whether a given product unit's age is a factor in transfusion efficacy, specifically on whether "older" 
             blood directly or indirectly increases risks of complications. Studies have not been consistent on answering this question,
              with some showing that older blood is indeed less effective but with others showing no such difference; nevertheless, as storage time remains
               the only available way to estimate quality status or loss, a first-in-first-out inventory management approach is standard presently. 
               It is also important to consider that there is large variability in storage results for different donors, which combined with limited 
               available quality testing, poses challenges to clinicians and regulators seeking reliable indicators of quality for blood products and 
               storage systems. Transfusions of platelets are comparatively far less numerous, but they present unique storage/management issues. Platelets may only be stored for
 7 days,due largely to their greater potential for contamination, which is in turn due largely to a higher storage temperature.</p>
